#include <bits/stdc++.h>

using namespace std;

int main() {

	// Data Types
	cout << "-_-_-_-_-_-_-_-_-_-_-_- Data Types -_-_-_-_-_-_-_-_-_-_-_-" << endl;

	int var1 = 12;
	cout << "value of var1 is " << var1 << ", size of int is " << sizeof(var1) << endl;

	float var2 = 14.34;
	cout << "value of var2 is " << var2 << ", size of float is " << sizeof(var2) << endl;

	char var3 = 'e';
	cout << "value of var3 is " << var3 << ", size of char is " << sizeof(var3) << endl;

	bool var4 = 1;
	cout << "value of var4 is " << var4 << ", size of bool is " << sizeof(var4) << endl;

	// Type Modifiers
	cout << "-_-_-_-_-_-_-_-_-_-_-_- Type Modifiers -_-_-_-_-_-_-_-_-_-_-_-" << endl;

	signed int var5;
	cout << "size of signed int is " << sizeof(var5) << endl;

	unsigned int var6;
	cout << "size of unsigned int is " << sizeof(var6) << endl;

	short int var7;
	cout << "size of short int is " << sizeof(var7) << endl;

	long long var8;
	cout << "size of long int is " << sizeof(var8) << endl;

	return(0);
}